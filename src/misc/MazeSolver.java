	public static void print_maze() {

		System.out.println();

		for (int row = 0; row < grid.length; row++) {
			for (int column = 0; column < grid[row].length; column++)
				System.out.print(grid[row][column] + " ");
			System.out.println();
		}

		System.out.println();

	} 

	public static boolean solve(int row, int column) {
		boolean check = false;
		if (check(row, column)) {
			if (row == grid.length - 1 && column == grid[0].length - 1) {
				grid[row][column] = 8;
				check = true;
			} else {
				grid[row][column] = 0;
				check = solve(row + 1, column);
				if (!check) {
					check = solve(row, column + 1);
				}
				if (!check) {
					check = solve(row - 1, column);
				}
				if (!check) {
					check = solve(row, column - 1);
				}
				if(check) {
					grid[row][column] = 8;
				}
			}
		}
		return check;
	}
	
	public static boolean check(int row, int column) {
		if (row < grid.length && column < grid[0].length && row >= 0 && column >= 0) {
			
			if (grid[row][column] == 1) {
				return true;
			}
		}
		return false;
	}
